<?php

#OPERADOR DE INCREMENTAÇÃO E DECREMENTAÇÃO

$a = 10 ;
$b = 5;

# INCREMENTO
echo "Valor da variavel ".$a; 
echo "<br>";
echo "Valor da variavel " .++$a ." (pré-incremento)";

echo "<br>";

echo "Valor inicial da variavel ".$a++; //10  
echo "<br>";
echo "Valor da variavel " .$a ." (pós-incremento)";

echo "<hr>";

# DECREMENTO
echo "Valor da variavel ".$b; 
echo "<br>";
echo "Valor da variavel " .--$b ." (pré-decremento)";

echo "<br>";

echo "Valor inicial da variavel ".$b--; //10  
echo "<br>";
echo "Valor da variavel " .$b ." (pós-decremento)";











?>